export type IconName = keyof typeof iconSvgStrings;

// React component that renders an icon from the shared SVG strings.
// Uses dangerouslySetInnerHTML so the exact same SVG is used in both
// the WYSIWYG editor and the export handler.
export function Icon({
  name,
  className,
  size,
}: {
  name: IconName;
  className?: string;
  size?: number;
}) {
  const svg = iconSvgStrings[name];
  if (!svg) return null;

  // If a custom size is requested, replace the width/height in the SVG string
  let finalSvg = svg;
  if (size) {
    finalSvg = finalSvg
      .replace(/width="\d+"/, `width="${size}"`)
      .replace(/height="\d+"/, `height="${size}"`)
  }

  return (
    <span
      className={`inline-flex items-center shrink-0 ${className || ""}`}
      dangerouslySetInnerHTML={{ __html: finalSvg }}
    />
  );
}

// Get the SVG string for a social platform, with a specific color applied
export function getSocialIconSvg(platform: string, color?: string): string {
  const p = platform.toLowerCase();
  let key: IconName = "globe";
  if (p.includes("github")) key = "github";
  else if (p.includes("linkedin")) key = "linkedin";
  else if (p.includes("medium")) key = "medium";
  else if (p.includes("researchgate")) key = "researchgate";

  let svg = iconSvgStrings[key];
  if (color) {
    svg = svg.replace(/stroke="currentColor"/g, `stroke="${color}"`)
             .replace(/fill="currentColor"/g, `fill="${color}"`);
  }
  return svg;
}

// Get a colored version of any icon SVG string (for export)
export function getIconSvg(name: IconName, color?: string): string {
  let svg = iconSvgStrings[name];
  if (color) {
    svg = svg.replace(/stroke="currentColor"/g, `stroke="${color}"`)
             .replace(/fill="currentColor"/g, `fill="${color}"`);
  }
  return svg;
}

// Map of social platform names to icon names
export const socialPlatformIcons: Record<string, IconName> = {
  GitHub: "github",
  Portfolio: "globe",
  LinkedIn: "linkedin",
  Medium: "medium",
  ResearchGate: "researchgate",
};

export function getSocialIconName(platform: string): IconName {
  return socialPlatformIcons[platform] || "globe";
}
